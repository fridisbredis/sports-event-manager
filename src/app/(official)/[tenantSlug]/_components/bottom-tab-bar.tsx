export function BottomTabBar({ tenantSlug }: Props) {
  const pathname = usePathname()
  const { t } = useTranslation('official')

  const tabs = [
    { href: `/${tenantSlug}/home`, label: t('nav.home'), Icon: HomeIcon },
    { href: `/${tenantSlug}/schedule`, label: t('nav.mySchedule'), Icon: CalendarIcon },
    { href: `/${tenantSlug}/event-info`, label: t('nav.eventInfo'), Icon: InfoIcon },
    { href: `/${tenantSlug}/announcements`, label: t('nav.announcements'), Icon: BellIcon },
    { href: `/${tenantSlug}/account`, label: t('nav.account'), Icon: PersonIcon },
  ]

  return (
    <nav className="fixed bottom-0 inset-x-0 bg-white border-t border-gray-200 z-40">
      <div className="flex">
        {tabs.map(({ href, label, Icon }) => {
          const active = pathname === href || pathname.startsWith(href + '/')
          return (
            <Link
              key={href}
              href={href}
              className={`flex-1 flex flex-col items-center justify-center py-2 gap-0.5 text-[10px] font-medium transition-colors ${
                active ? 'text-gray-900' : 'text-gray-400 hover:text-gray-600'
              }`}
            >
              <Icon filled={active} />
              <span>{label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
